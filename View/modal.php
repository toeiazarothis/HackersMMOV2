                <h2>Comment éviter l'effet du virus "delete ip history".</h2>
                <p class="item-intro text-muted">
                  Rappel du virus. <br>
                  Le virus "delete ip history" sert à effacer la liste d'ip que vous possédez. Il réagit au moment où vous vous connectez au jeu. Cependant, il est possible de conserver sa liste de 2 façons différentes.
                </p>
                <img class="img-responsive img-centered" src="" alt="">
                <p>
                    1/ la méthode simple
                    Si vous avez un ordinateur ou une tablette, téléchargez l'émulateur Android nommé Nox, que vous trouverez sur le site bignox. Installez le sur votre pc ou tablette. Il s'agit d'un programme qui va simuler dans votre pc un périphérique Android. Une fois nox installé, lancez le et allez chercher hackers dans Google play, comme sur votre smartphone. Installez le, connectez vous à votre compte et c'est tout!
                    Vous constaterez que votre liste d'ip sera vide dans la version emulée du jeu. C'est normal, le jeu sauvegarde la liste d'ip en local ! Ce qui signifie que vous pouvez avoir plusieurs listes si vous avez plusieurs périphériques Android, comme par exemple, un smartphone et un émulateur !
                    Ensuite, il ne vous reste plus qu'à vous connecter d'abord sur l'emulateur. Si vous avez le virus, il supprimera la liste qui se trouve dans la version emulée du jeu. Donc vous pourrez aller supprimer le virus de vos fichiers, et ensuite vous connecter sereinement sur votre périphérique habituel, et comme vous aurez supprimé le virus via l'emulateur, votre liste principale sera sauve !<br>
                    <hr>
                    2/ la méthode chiante
                    ! Nécessite un smartphone rooté !
                    La liste d'ip est sauvegardée en local sur votre téléphone, dans un dossier dont le nom comprend okitoo. Malheureusement, ce dossier n'est visible et accessible que si votre smartphone est rooté. Donc, assumons que ce soit le cas. Allez simplement dans le dossier de données du jeu, et faites un backup du XML contenant la liste d'ip avant de vous connecter. Si vous avez le virus, supprimez le de votre gateway, puis quittez le jeu, restaurez le backup du XML, et relancez le jeu. Votre liste est revenue !

                    Pour tout ce qui concerne le rootage, Google est votre ami. Sachez que cette pratique est risquée, elle peut détruire votre téléphone ou exposer vos données personnelles à un pirate, donc lisez beaucoup avant de rooter ! La méthode 1 ne nécessite pas de root.

                    En espérant que ça vous aide
